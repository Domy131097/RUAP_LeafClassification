package hr.unios.ferit.leafclassification.Model;

public class TrainSample {
    private String name;
    private String data;

    public TrainSample(String name, String data) {
        this.name = name;
        this.data = data;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
